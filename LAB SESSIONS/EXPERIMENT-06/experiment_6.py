# Upload video
from google.colab import files
uploaded = files.upload()

import cv2
from IPython.display import Video, display

# Get uploaded filename
filename = list(uploaded.keys())[0]

# Open the uploaded video
cap = cv2.VideoCapture(filename)

if not cap.isOpened():
    print("Error: Unable to open the video.")
else:
    # Get video properties
    fps = cap.get(cv2.CAP_PROP_FPS)
    width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    # Create VideoWriters
    fourcc = cv2.VideoWriter_fourcc(*'mp4v')

    slow_out = cv2.VideoWriter(
        "Slow_Motion.mp4",
        fourcc,
        max(fps / 2, 1),      # Half speed
        (width, height)
    )

    fast_out = cv2.VideoWriter(
        "Fast_Motion.mp4",
        fourcc,
        fps * 2,              # Double speed
        (width, height)
    )

    frame_count = 0

    while True:
        ret, frame = cap.read()
        if not ret:
            break

        # Slow Motion (duplicate every frame)
        slow_out.write(frame)
        slow_out.write(frame)

        # Fast Motion (keep alternate frames)
        if frame_count % 2 == 0:
            fast_out.write(frame)

        frame_count += 1

    cap.release()
    slow_out.release()
    fast_out.release()

    print("Processing Completed Successfully!")

    # Display output videos
    print("\nSlow Motion Video")
    display(Video("Slow_Motion.mp4", embed=True))

    print("\nFast Motion Video")
    display(Video("Fast_Motion.mp4", embed=True))

    # Download output videos
    files.download("Slow_Motion.mp4")
    files.download("Fast_Motion.mp4")